export const getServerSideProps: GetServerSideProps = async (ctx) => {
  const { traveller_token } = parseCookies(ctx);
  if (!traveller_token) return { props: {} };

  const { token, user } = JSON.parse(traveller_token);

  return {
    redirect: {
      destination: '/dashboard',
      permanent: false,
    },
    props: { token, user },
  };
};

